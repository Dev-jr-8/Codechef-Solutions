import java.util.*;

class Codechef
{
    public static void main(String[] args) throws java.lang.Exception
    {
        try {
            Scanner sc = new Scanner(System.in);
            int test = sc.nextInt();
            while(test-->0)
            {
                int x =sc.nextInt();
                int y = sc.nextInt();
                int z = sc.nextInt();
                System.out.println(x-y+z);
            }
            
        } catch(Exception e) {
            return;
        }
    }
}
